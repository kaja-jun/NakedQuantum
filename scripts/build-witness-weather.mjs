/**
 * Generate witness-weather.js from witness-weather-blueprint.md
 * Run: node scripts/build-witness-weather.mjs
 */
import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const md = readFileSync(join(root, 'witness-weather-blueprint.md'), 'utf8');

const re = /\*\*(\w+) × (\w+)\*\*\s*\n\s*```\s*\n([\s\S]*?)```/g;
const matrix = {};
let m;
while ((m = re.exec(md)) !== null) {
  const signal = m[1];
  const voice = m[2];
  const lines = m[3]
    .split('\n')
    .map(function (l) { return l.trim(); })
    .filter(function (l) { return l.startsWith('"') && l.endsWith('"'); })
    .map(function (l) { return l.slice(1, -1); });
  if (!matrix[signal]) matrix[signal] = {};
  matrix[signal][voice] = lines;
}

const signals = ['orbit', 'bridge', 'resurgence', 'denial', 'absence'];
const voices = ['rilke', 'rovelli', 'weil', 'socrates', 'descartes', 'camus', 'einstein', 'spinoza', 'laotzu', 'wittgenstein'];
for (const s of signals) {
  if (!matrix[s]) throw new Error('Missing signal type: ' + s);
  for (const v of voices) {
    if (!matrix[s][v] || !matrix[s][v].length) throw new Error('Missing ' + s + ' × ' + v);
  }
}

const header = `/* jshint esversion: 11 */
/* global window */
/**
 * Witness weather + cues — pure utility (see witness-weather-blueprint.md).
 * Generated by scripts/build-witness-weather.mjs — edit blueprint, re-run script.
 */
(function (global) {
  'use strict';

  var VOICE_BUCKETS = {
    high: ['weil', 'rilke', 'rovelli'],
    mid: ['socrates', 'descartes', 'camus'],
    low: ['einstein', 'spinoza', 'laotzu', 'wittgenstein']
  };

  var WEATHER_LABELS = {
    front: 'front moving in',
    pressure: 'pressure building',
    electrical: 'electrical',
    after_storm: 'after the storm',
    drought: 'long silence'
  };

  var WEATHER_PRIORITY = ['electrical', 'pressure', 'front', 'after_storm', 'drought', 'calm'];

  /** PWA: absence matrix present; wire when void_hints ships (Track B). */
  var ENABLE_ABSENCE_CUES = false;

  var CUES_MATRIX = ${JSON.stringify(matrix, null, 2)};

  function cueRetireKey(type, term) {
    return String(type) + '|' + String(term);
  }

  function clamp01(n) {
    if (n == null || isNaN(n)) return 0;
    return Math.max(0, Math.min(1, n));
  }

  function computeWeatherState(snap, ctx) {
    ctx = ctx || {};
    if (!snap) return { state: 'calm', label: null, intensity: 0 };
    if (ctx.post_craft) return { state: 'calm', label: null, intensity: 0 };

    var pv = snap.posture_vector || {};
    var coherence = pv.coherence != null ? pv.coherence : 0;
    var resistance = pv.resistance != null ? pv.resistance : 0;
    var openN = snap.open_bridges_count != null
      ? snap.open_bridges_count
      : (snap.open_bridges ? snap.open_bridges.length : 0);
    var orbitN = (snap.perpetual_orbit_terms && snap.perpetual_orbit_terms.length) || 0;
    var resurgent = (snap.half_life && snap.half_life.resurgent) || [];
    var resistanceDelta = ctx.resistance_delta != null ? ctx.resistance_delta : 0;
    var daysSinceWrite = ctx.days_since_write != null ? ctx.days_since_write : null;
    var prevTurbulent = ctx.previous_weather_turbulent === true;

    var candidates = [];

    if (resurgent.length) {
      candidates.push({
        state: 'electrical',
        intensity: clamp01(0.5 + resurgent.length * 0.15)
      });
    }
    if (openN >= 2 || orbitN >= 3) {
      candidates.push({
        state: 'pressure',
        intensity: clamp01(Math.max(openN / 4, orbitN / 5))
      });
    }
    if (resistanceDelta >= 0.3) {
      candidates.push({
        state: 'front',
        intensity: clamp01(resistanceDelta)
      });
    }
    if (prevTurbulent && resistanceDelta <= -0.15 && coherence >= 0.5) {
      candidates.push({
        state: 'after_storm',
        intensity: clamp01(0.4 + Math.abs(resistanceDelta))
      });
    }
    if (daysSinceWrite != null && daysSinceWrite >= 14) {
      candidates.push({
        state: 'drought',
        intensity: clamp01(Math.min(1, daysSinceWrite / 30))
      });
    }

    if (coherence >= 0.85 && resistance < 0.3 && openN === 0 && !resurgent.length) {
      return { state: 'calm', label: null, intensity: 0 };
    }

    if (!candidates.length) {
      return { state: 'calm', label: null, intensity: 0 };
    }

    candidates.sort(function (a, b) {
      return WEATHER_PRIORITY.indexOf(a.state) - WEATHER_PRIORITY.indexOf(b.state);
    });
    var pick = candidates[0];
    return {
      state: pick.state,
      label: WEATHER_LABELS[pick.state] || pick.state,
      intensity: pick.intensity
    };
  }

  function selectVoice(intensity, opts) {
    opts = opts || {};
    var bucket;
    if (intensity > 0.66) bucket = VOICE_BUCKETS.high;
    else if (intensity >= 0.33) bucket = VOICE_BUCKETS.mid;
    else bucket = VOICE_BUCKETS.low;

    if (opts.first_cue_on_term && !opts.term_engaged_before) {
      bucket = VOICE_BUCKETS.mid.concat(VOICE_BUCKETS.low);
    }

    return bucket[Math.floor(Math.random() * bucket.length)];
  }

  function parseDenialTerms(snap) {
    var out = [];
    (snap.anomalies || []).forEach(function (a) {
      if (typeof a === 'string' && a.indexOf('denial_sediment:') === 0) {
        out.push(a.slice('denial_sediment:'.length));
      }
    });
    return out;
  }

  function firstOpenBridgeTerm(snap) {
    var obs = snap.open_bridges || [];
    for (var i = 0; i < obs.length; i++) {
      var terms = obs[i].terms;
      if (terms && terms.length) return terms[0];
    }
    return null;
  }

  function buildCandidates(snap) {
    var list = [];
    if (!snap) return list;

    if (snap.perpetual_orbit_terms && snap.perpetual_orbit_terms.length) {
      list.push({ type: 'orbit', term: snap.perpetual_orbit_terms[0] });
    }
    var bridgeTerm = firstOpenBridgeTerm(snap);
    if (bridgeTerm) list.push({ type: 'bridge', term: bridgeTerm });

    var resurgent = (snap.half_life && snap.half_life.resurgent) || [];
    if (resurgent.length) list.push({ type: 'resurgence', term: resurgent[0] });

    var denial = snap.denial_sediment_terms || parseDenialTerms(snap);
    if (denial.length) list.push({ type: 'denial', term: denial[0] });

    if (ENABLE_ABSENCE_CUES && snap.void_hints && snap.void_hints.length) {
      list.push({ type: 'absence', term: snap.void_hints[0] });
    }

    return list;
  }

  function cuesAllowedForWeather(weather) {
    if (!weather) return false;
    if (weather.state === 'calm' || weather.state === 'drought') return false;
    return true;
  }

  function generateWitnessCues(snap, opts) {
    opts = opts || {};
    var weather = computeWeatherState(snap, opts.context);
    if (!cuesAllowedForWeather(weather)) return [];

    var retired = opts.retiredKeys || {};
    var cues = [];
    var usedSignals = new Set();
    var candidates = buildCandidates(snap);

    for (var i = 0; i < candidates.length; i++) {
      if (cues.length >= 3) break;
      var candidate = candidates[i];
      if (usedSignals.has(candidate.type)) continue;

      var key = cueRetireKey(candidate.type, candidate.term);
      if (retired[key]) continue;

      var voice = selectVoice(weather.intensity, {
        first_cue_on_term: opts.first_cue_on_term,
        term_engaged_before: opts.engagedTerms && opts.engagedTerms[candidate.term]
      });
      var pool = CUES_MATRIX[candidate.type] && CUES_MATRIX[candidate.type][voice];
      if (!pool || !pool.length) continue;

      var question = pool[Math.floor(Math.random() * pool.length)]
        .replace(/\\{term\\}/g, candidate.term);

      cues.push({
        source_type: candidate.type,
        source_term: candidate.term,
        question_text: question
      });
      usedSignals.add(candidate.type);
    }

    return cues;
  }

  var api = {
    VOICE_BUCKETS: VOICE_BUCKETS,
    CUES_MATRIX: CUES_MATRIX,
    ENABLE_ABSENCE_CUES: ENABLE_ABSENCE_CUES,
    cueRetireKey: cueRetireKey,
    computeWeatherState: computeWeatherState,
    generateWitnessCues: generateWitnessCues,
    buildCandidates: buildCandidates
  };

  if (typeof module !== 'undefined' && module.exports) {
    module.exports = api;
  }
  global.WitnessWeather = api;
})(typeof window !== 'undefined' ? window : globalThis);
`;

writeFileSync(join(root, 'witness-weather.js'), header);
console.log('Wrote witness-weather.js —', signals.length, 'signals ×', voices.length, 'voices');
